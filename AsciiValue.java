public class AsciiValue
{
public static void main(String[] args)
{
int ch1 = 'A';
int ch2 = 'Z';
int ch3 = 'a';
int ch4 = 'z';
System.out.println("The ASCII Value of A is " + ch1);
System.out.println("The ASCII Value of Z is " + ch2);
System.out.println("The ASCII Value of a is " + ch3);
System.out.println("The ASCII Value of z is " + ch4);
}
}